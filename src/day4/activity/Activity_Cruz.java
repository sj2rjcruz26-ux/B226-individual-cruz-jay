package day4.activity;

public class Activity_Cruz {

    public static void main(String[] args) {
        // Boolean
        boolean isStudent = true;
        // Byte
        byte age = 22;
        // Short
        short year = 2026;
        // Int
        int population = 1000000;
        // Long
        long phoneNumber = 9305155243l;
        // Float
        float height = 5.7f;
        // Double
        double weight = 51.15;
        // Char
        char grade = 'A';
        // String
        String name = "Ronald Jay Cruz";

        // Print values
        System.out.println("Boolean value: " + isStudent);
        System.out.println("Byte value: " + age);
        System.out.println("Short value: " + year);
        System.out.println("Int value: " + population);
        System.out.println("Long value: " + phoneNumber);
        System.out.println("Float value: " + height);
        System.out.println("Double value: " + weight);
        System.out.println("Char value: " + grade);
        System.out.println("String value: " + name);

    }
}
